package conditional;

public class SwitchCase {

	public static void main(String[] args) {
		char grade ='F';
		switch(grade)
		{
		case 'A':
			System.out.println("Exellent");
		case 'B':
			System.out.println("well Done");
			
		case 'C':
			System.out.println("Better Try again");
			
		default:
			System.out.println("Enter valid Grade");
		

	}

}
}
